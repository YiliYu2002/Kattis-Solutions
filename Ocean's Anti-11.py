num = int(input())
div = 1000000007
# div = math.pow(10, 9) + 7
lis = {0: 1, 1: 2, 2: 3}
for i in range(num):
    length = int(input())
    if length != 0 or 1 or 2:
        for j in range(3, length + 1):
            if j not in lis:
                lis[j] = lis[j - 1] + lis[j - 2]
    print(lis[length] % div)
